fun main(args: Array<String>) {
    println("Number Range")
    for (nambari in 3..15){
        println(nambari)
    }
    println("character range")//characters uses single quote
    for (alphabet in 'a'..'z'){
        println(alphabet)
    }
    val twototen=2..10
    for (n in twototen.reversed()){
        println(n)
    }


}